    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter product name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jComboBox2.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select a category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String name = jTextField1.getText();
            String category = jComboBox2.getSelectedItem().toString();

            try {
                ResultSet rs = MYSQLS.search("SELECT * FROM `product` INNER JOIN `category` ON `category_id`=`category`.`id` WHERE `product`.`Name`='" + name + "' AND `category`.`Name`='" + category + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Product aleready exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    ResultSet rs2 = MYSQLS.search("SELECT `id` FROM `category` WHERE `Name`='" + category + "'");
                    rs2.next();

                    String category_id = rs2.getString("id");

                    MYSQLS.iud("INSERT INTO `product` (`Name`,`category_id`) VALUES('" + name + "','" + category_id + "')");

                    jTextField1.setText("");
                    jComboBox2.setSelectedIndex(0);

                    loadProducts();
                    JOptionPane.showMessageDialog(this, "New Product added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

